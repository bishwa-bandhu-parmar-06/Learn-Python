# Python lists are containers to store a set of values of any data type.

friends = ["Apple", "Orange", 3, 4.55, True]
print(friends)
print(friends[0])
friends[1] = "Grapes" #unlike strings list are mutable
print(friends)
print(friends[1:4])
print(friends[0:4])
print(friends[:4])
print(friends[1:])
print(friends[:])